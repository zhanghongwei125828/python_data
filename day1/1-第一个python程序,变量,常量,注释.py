# 变量
    #变量就是将一些运算的中间结果暂存到内存中,以便后续代码调用.
    # 1,变量必须由字母,数字,下划线任意组合,且不能任意组合
    # 2,不能是python中的关键字
    # 3,变量具有可描述性.
    # 4,变量不能是中文,虽然中文不会报错
a = 10
b = 'hello,zhangsan'
print(a)
print(b)


# 常量
    #常量就是一直不变的量,在python语言里没有绝对的常量
    #变量名大写
NUM = 1994
print(NUM)


