# 字符串索引切片

# 字符串的索引与切片
s = "abcdefghigklmn"
# 索引
    #取第一位
s1 = s[0]
print(s1)
    #取最后一位
s2 = s[-1]
print(s2)

################################################################################

# 切片   # 原则顾头不顾尾
    # 取abcd
s3 = s[0:4]
print(s3)

    # 取全部字符串
s4 = s[:]
print(s4)

################################################################################

# 步长
# 取整段字符串,两位取一个
s5 = s[0:14:2]
print(s5)